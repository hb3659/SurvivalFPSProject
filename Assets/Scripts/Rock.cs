using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Rock : MonoBehaviour
{
    [SerializeField]
    private int hp;                         // 바위의 체력
    [SerializeField]
    private float destroyTime;              // 파편 제거 시간
    [SerializeField]
    private float effectDestroyTime;        // 이펙트 제거 시간

    [SerializeField]
    private Collider collider;              // 구체 콜라이더

    // 필요한 게임 오브젝트
    [SerializeField]
    private GameObject go_rock;             // 일반 바위
    [SerializeField]
    private GameObject go_debris;           // 부서진 바위
    [SerializeField]
    private GameObject go_effect_prefab;    // 채굴 이펙트
    [SerializeField]
    private GameObject go_rock_item;        // 돌멩이 아이템

    [SerializeField]
    private int count;

    // 필요한 사운드 이름
    [SerializeField]
    private string strike_Sound;
    [SerializeField]
    private string destroy_Sound;

    public void Mining()
    {
        SoundManager.instance.PlaySE(strike_Sound);

        GameObject clone = Instantiate(go_effect_prefab, collider.bounds.center, Quaternion.identity);
        Destroy(clone, effectDestroyTime);

        hp--;

        if (hp <= 0)
            Destruction();
    }

    void Destruction()
    {
        SoundManager.instance.PlaySE(destroy_Sound);

        collider.enabled = false;

        for (int i = 0; i < count; i++)
        {
            Instantiate(go_rock_item, go_rock.transform.position, Quaternion.identity);
        }
        
        Destroy(go_rock);

        go_debris.SetActive(true);
        Destroy(go_debris, destroyTime);
    }
}
